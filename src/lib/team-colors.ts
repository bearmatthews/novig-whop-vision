/**
 * Get primary color for a team
 */
export function getTeamColor(teamName: string, league: string): string | null {
  const colors = TEAM_COLORS[league];
  if (!colors) return null;
  return colors[teamName] || null;
}

/**
 * Get team color from event
 */
export function getEventTeamColor(event: any): string | null {
  const teams = event.description.match(/^(.+?)\s+@\s+(.+?)$/);
  if (!teams) return null;
  
  const homeTeam = teams[2].trim();
  const league = event.game?.league;
  
  if (!league) return null;
  return getTeamColor(homeTeam, league);
}
